# Show union, intersection, difference of 2 sets.

set1= {"orange","pink","yellow","brown"}
set2 = {"red", "blue", "green", "yellow"}

set3 = set1.union(set2)
set4 = set1.intersection(set2)
set5 = set1.difference(set2)

print(set3)                #union of sets
print(set4)                #intersection of sets
print(set5)                #difference

